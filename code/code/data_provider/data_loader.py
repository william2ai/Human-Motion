def time_features_a(df_stamp):
    # Convert the first column (time in seconds) to datetime
    df_stamp[0] = pd.to_datetime(df_stamp[0], unit='s')
    
    # Extract features from the datetime column
    hour = df_stamp[0].dt.hour
    minute = df_stamp[0].dt.minute
    second = df_stamp[0].dt.second
    
    # Example of cyclical encoding for hour and minute
    hour_sin = np.sin(2 * np.pi * hour / 24)
    hour_cos = np.cos(2 * np.pi * hour / 24)
    minute_sin = np.sin(2 * np.pi * minute / 60)
    minute_cos = np.cos(2 * np.pi * minute / 60)

    # Create a DataFrame with the extracted features
    features = pd.DataFrame({
        'hour_sin': hour_sin,
        'hour_cos': hour_cos,
        'minute_sin': minute_sin,
        'minute_cos': minute_cos,
        'second': second
    })
    
    return features

